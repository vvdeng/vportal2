	var initOptions = function (oSelect,fatherId,oRegion)
	{
		if(null == oSelect) return false; 
		//var oTimeElf = new ElfTimeCheck();
		var aOption = new Option("","");
		eval("oSelect.options[oSelect.options.length]=aOption");
			
		// var oRegionTime = new ElfTimeCheck();
		 var subRegions = g_oRegionMgr.getSubRegions(fatherId);
		 //EShowDebug("oRegionTime:"+oRegionTime.CheckHere());
		 if ( null == subRegions)
		 {
				return false;
		 }
		 var selectIndex = 0;
		 var iIndex = 1;
		 
		 var oDomFragment = document.createDocumentFragment();
		 for (elem in subRegions)
		 {
		 	  if(subRegions[elem] == null) continue;	
		      if(subRegions[elem].isPreDelete())
		      	continue;
				if( oRegion != null && oRegion.getId() == subRegions[elem].getId())
				{ 
					selectIndex = iIndex;
				}
				var oOption = document.createElement("OPTION");
				oOption.innerHTML = subRegions[elem].getName();
				oOption.setAttribute("value",subRegions[elem].getId());
				oDomFragment.appendChild(oOption); 
				iIndex++;
		 }
		 oSelect.appendChild(oDomFragment);
		 //EShowDebug("Len:"+oSelect.options.length);
		 oSelect.selectedIndex = selectIndex;
		 //EShowDebug("initOptions:"+oTimeElf.CheckHere());
	}
	
	
