public class ListLogAppender implements LogAppender {

	/**
	 * ???????????t?????????
	 */
	private List<LogAppender> appenderList;

	/**
	 * ???????????
	 * @param auditLog????????
	 * @see 
	 */
	public void append(AuditLog auditLog) {
		if (this.appenderList != null) {
			for (LogAppender appender : this.appenderList)
				appender.append(auditLog);
		}
	}
	
	public void setAppenderList(List<LogAppender> appenderList) {
		this.appenderList = appenderList;
	}
}
