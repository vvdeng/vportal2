public class DynamicMapContext extends MapContext {

	public DynamicMapContext(Map target) {
		super(target);
	}

	public DynamicMapContext() {
		super();
	}

	public DynamicMapContext(Object... values) {
		for (int i = 0; i < values.length; i++) {
			add("p" + i, values[i]);
		}
	}
}
