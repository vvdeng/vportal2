public class NavigatorItemTag extends SimpleTagSupport {
	private String id;
	private String desc;
	private String link;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	@Override
	public void doTag() throws JspException, IOException {
		NavigatorItemPrinter navigatorItemPrinter = new NavigatorItemPrinter(getJspBody(),
				getJspContext());
		navigatorItemPrinter.printNavigatorItem(id,desc,link);
	}

}
