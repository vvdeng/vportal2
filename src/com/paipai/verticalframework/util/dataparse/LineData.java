public class LineData {
	
	private int line;
	private Object[] data;
	
	public LineData(int line, Object[] data) {
		this.line = line;
		this.data = data;
	}
	
	public int getLine() {
		return line;
	}
	public void setLine(int line) {
		this.line = line;
	}
	public Object[] getData() {
		return data;
	}
	public void setData(Object[] data) {
		this.data = data;
	}
}
